package Book2.chapter1;

public class CaseApp {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++)
            System.out.println("Hi");
    }
}
//Hi will be printed 5 times i = 0 and we start counting at 0 so as long as i is less than 5 it will print.
// The i++ means add 1 to i while i is less than 5